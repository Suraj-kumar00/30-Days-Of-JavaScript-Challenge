// ES6 + Features:

// Template Literals:
// task: Use template literals to create a string that includes variables for a person's name and age, and log the string to the console.

let name = "Suraj";
let age = 21;


// The literal in JavaScript provide a way to work with strings that can include embedded expressions and variables. They are defined using backticks (`) instead of single quotes (') or double quotes (").

const message = `Hello, there my name is ${name} and I'm ${age} years old.`
console.log(message);
