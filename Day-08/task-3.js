// Destructuring 

// task: Use array destructuring to extract the first and Second elements from an array or numbers and log them to the console.

let arr = [1,2,3,4,5];

const [first,scond] = arr; // syntax of array destructuring and here the fist two elements of the arr will be assigned to first and scond.


console.log("first element:",first); // this will give first element of the array
console.log("second element:",scond); // this will give the second element of the array