// WAP to compare two numbers using >= and <= operators

let num1 = 56;

let num2 = 57;

// here we are comparing two numbers using >= and <= operators using if condition

if(num1 >= num2){
    console.log(`${num1} is greater than or equal to ${num2}`);
}

if(num1 <= num2){
    console.log(`${num1} is less than or equal to ${num2}`);
}
