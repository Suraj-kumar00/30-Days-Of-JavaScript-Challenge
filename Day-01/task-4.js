// Create variable of different data types and log each variable's type using typeof operator.

let name = "Suraj"; // this is string data type
const number = 1234; // this is number data type
boolean = true;     // this is boolean data type

// creating object

let bro = {
    name: "Suraj",
    age: 21
}

// creating array

let countries = ["India", "USA", "UK"];


// console log the type of each variable

console.log('name:', name, 'typeof:',typeof name);
console.log('number:', number, 'typeof:',typeof number);
console.log('boolean:', boolean, 'typeof:',typeof boolean);
console.log('bro:', bro, 'typeof:',typeof bro);
console.log('countries:', countries, 'typeof:',typeof countries);
